const topics = ["All", "Monuments", "Backwaters", "Caves & Temples", "Yoga", "Cuisine", "Festivals"];

/* Supported languages & labels */
const LANGS = [
  { code: "en", label: "EN" },
  { code: "hi", label: "‡§π‡§ø‡§Ç‡§¶‡•Ä" },
  { code: "te", label: "‡∞§‡±Ü‡∞≤‡±Å‡∞ó‡±Å" },
  { code: "ta", label: "‡Æ§‡ÆÆ‡Æø‡Æ¥‡Øç" },
  { code: "kn", label: "‡≤ï‡≤®‡≥ç‡≤®‡≤°" },
  { code: "ml", label: "‡¥Æ‡¥≤‡¥Ø‡¥æ‡¥≥‡¥Ç" },
  { code: "bn", label: "‡¶¨‡¶æ‡¶Ç‡¶≤‡¶æ" },
];

export default function Dashboard({ user, onLogout }) {
  const [selectedTopic, setSelectedTopic] = useState("All");
  const [query, setQuery] = useState("");
  const [selectedCard, setSelectedCard] = useState(null);
  const [theme, setTheme] = useState(() => localStorage.getItem("theme") || "dark");
  const [lang, setLang] = useState("en"); // current translation language for modal

  useEffect(() => {
    document.documentElement.setAttribute("data-theme", theme);
    localStorage.setItem("theme", theme);
  }, [theme]);

  // reset language when modal closes
  useEffect(() => {
    if (!selectedCard) setLang("en");
  }, [selectedCard]);

  // filtered list based on selectedTopic + query
  const filtered = useMemo(() => {
    let list = cards.slice();
    if (selectedTopic !== "All") list = list.filter((c) => c.category === selectedTopic);
    if (query.trim()) {
      const q = query.toLowerCase();
      list = list.filter((c) => c.title.toLowerCase().includes(q) || c.desc.toLowerCase().includes(q));
    }
    return list;
  }, [selectedTopic, query]);

  function openDetails(card) {
    setSelectedCard(card);
    setLang("en"); // reset to English on open
  }

  function closeDetails() {
    setSelectedCard(null);
    if (window.speechSynthesis) window.speechSynthesis.cancel();
  }

  function speakText(card) {
    if (!card) return;
    if (!("speechSynthesis" in window)) {
      alert("Your browser does not support Speech Synthesis.");
      return;
    }
    const utter = new SpeechSynthesisUtterance(`${card.title}. ${card.longDesc || card.desc}`);
    utter.lang = "en-US";
    window.speechSynthesis.cancel();
    window.speechSynthesis.speak(utter);
  }

  // returns translated description string for modal (falls back to English)
  function getTranslatedText(card) {
    if (!card) return "";
    if (lang === "en") return card.longDesc || card.desc;
    if (card.trans && card.trans[lang]) return card.trans[lang];
    return card.longDesc || card.desc;
  }

  return (
    <div className="dashboard">
      <Sidebar
        topics={topics}
        active={selectedTopic}
        onSelectTopic={(t) => setSelectedTopic(t)}
        onLogout={onLogout}
      />

      <div className="content">
        <div className="topbar" style={{ alignItems: "center", gap: 12 }}>
          <div className="welcome" style={{ fontSize: 22, color: "white", fontWeight: 700 }}>
            üôè <strong>Namaste, {user?.name || "Guest"}</strong>
          </div>

          <div style={{ marginLeft: "auto", display: "flex", gap: 10, alignItems: "center" }}>
            <input
              placeholder="Search (title or description)"
              value={query}
              onChange={(e) => setQuery(e.target.value)}
              style={{ padding: 8, borderRadius: 8, border: "1px solid rgba(255,255,255,0.12)" }}
            />

            <button
              onClick={() => setTheme((t) => (t === "dark" ? "light" : "dark"))}
              style={{ padding: "8px 12px", borderRadius: 8, border: "none", cursor: "pointer" }}
            >
              {theme === "dark" ? "Light" : "Dark"}
            </button>
          </div>
        </div>

        {/* India map navigation (clicking a region sets topic) */}
        <div style={{ margin: "12px 0 18px" }}>
          <IndiaMap onSelectRegion={(regionTopic) => setSelectedTopic(regionTopic)} />
        </div>

        <div style={{ marginBottom: 10, color: "rgba(255,255,255,0.9)" }}>
          Showing <strong>{filtered.length}</strong> results for <strong>{selectedTopic}</strong>
        </div>

        <div className="cards-grid">
          {filtered.map((c) => (
            <HeriCard key={c.id} {...c} onClick={() => openDetails(c)} />
          ))}
        </div>
      </div>

      {/* Detail Modal with Google Maps embed + translation buttons */}
      {selectedCard && (
        <div
          onClick={closeDetails}
          style={{
            position: "fixed",
            left: 0,
            top: 0,
            right: 0,
            bottom: 0,
            background: "rgba(0,0,0,0.5)",
            display: "flex",
            alignItems: "center",
            justifyContent: "center",
            zIndex: 9999,
            padding: 16,
          }}
        >
          <div
            onClick={(e) => e.stopPropagation()}
            style={{ width: 900, maxWidth: "100%", borderRadius: 12, background: "#fff", padding: 18, boxShadow: "0 24px 80px rgba(0,0,0,0.5)" }}
          >
            <div style={{ display: "flex", gap: 18 }}>
              <img
                src={selectedCard.img}
                alt={selectedCard.title}
                style={{ width: 340, height: 220, objectFit: "cover", borderRadius: 8 }}
                onError={(e) => {
                  e.target.onerror = null;
                  e.target.src = "https://via.placeholder.com/800x600?text=Image+Not+Found";
                }}
              />
              <div style={{ flex: 1 }}>
                <h2 style={{ margin: 0 }}>{selectedCard.title}</h2>

                {/* TRANSLATION BUTTONS */}
                <div style={{ marginTop: 10, display: "flex", gap: 8, flexWrap: "wrap", alignItems: "center" }}>
                  <div style={{ fontSize: 13, color: "#5b6b7a", fontWeight: 600 }}>Translate:</div>
                  {LANGS.map((L) => (
                    <button
                      key={L.code}
                      onClick={() => setLang(L.code)}
                      style={{
                        padding: "6px 10px",
                        borderRadius: 8,
                        border: lang === L.code ? "2px solid #1f8ed6" : "1px solid rgba(0,0,0,0.08)",
                        background: lang === L.code ? "#e8f6ff" : "#f7f9fb",
                        cursor: "pointer",
                        fontWeight: 700,
                        fontSize: 13,
                      }}
                    >
                      {L.label}
                    </button>
                  ))}
                </div>

                <p style={{ marginTop: 12, color: "#3b5460", minHeight: 44 }}>
                  {getTranslatedText(selectedCard)}
                </p>

                <div style={{ marginTop: 12, display: "flex", gap: 8 }}>
                  <button onClick={() => speakText(selectedCard)} style={{ padding: "8px 12px", borderRadius: 8 }}>
                    üîä Read Aloud
                  </button>
                  <a href={`https://en.wikipedia.org/wiki/${encodeURIComponent(selectedCard.title)}`} target="_blank" rel="noreferrer" style={{ padding: "8px 12px", borderRadius: 8, background: "#eef", textDecoration: "none" }}>
                    Read more
                  </a>
                </div>
              </div>
            </div>

            <div style={{ marginTop: 18 }}>
              <h4 style={{ marginBottom: 8 }}>Location</h4>
              <div style={{ height: 360, borderRadius: 8, overflow: "hidden", border: "1px solid #ddd" }}>
                <iframe
                  title="map"
                  src={`https://www.google.com/maps?q=${selectedCard.coords.lat},${selectedCard.coords.lng}&z=14&output=embed`}
                  style={{ width: "100%", height: "100%", border: 0 }}
                />
              </div>
            </div>

            <div style={{ marginTop: 12, textAlign: "right" }}>
              <button onClick={closeDetails} style={{ padding: "8px 12px", borderRadius: 8 }}>Close</button>
            </div>
          </div>
        </div>
      )}
    </div>
  );
}
